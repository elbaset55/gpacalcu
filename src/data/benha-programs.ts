export function getProgramById(id: string): BenhaProgram | undefined {
  return BENHA_PROGRAMS.find((p) => p.id === id);
}

export function getProgramsByDepartment(dept: string): BenhaProgram[] {
  return BENHA_PROGRAMS.filter((p) => p.department === dept);
}

export function getUniqueCourseCodes(program: BenhaProgram): string[] {
  const codes = new Set<string>();
  [...program.compulsoryPool, ...program.electivePool].forEach((c) => codes.add(c.code));
  return Array.from(codes);
}

export function findCourseInProgram(program: BenhaProgram, code: string): BenhaCourse | undefined {
  return (
    program.compulsoryPool.find((c) => c.code === code) ||
    program.electivePool.find((c) => c.code === code) ||
    program.freeOptionalPool.find((c) => c.code === code)
  );
}

export type CourseStatus = "completed" | "in-progress" | "pending" | "eligible";

export interface CourseAuditItem {
  course: BenhaCourse;
  type: "compulsory" | "elective" | "free" | "training" | "research";
  status: CourseStatus;
  userGrade?: string;
  userCredits?: number;
}

export interface DegreeAudit {
  programId: string;
  totalRequired: number;
  totalCompleted: number;
  totalRemaining: number;
  compulsoryRequired: number;
  compulsoryCompleted: number;
  electiveRequired: number;
  electiveCompleted: number;
  freeElectiveRequired: number;
  freeElectiveCompleted: number;
  trainingCompleted: boolean;
  researchCompleted: boolean;
  progressPercent: number;
  completedCourses: CourseAuditItem[];
  pendingCourses: CourseAuditItem[];
  specialRequirements: { label: string; met: boolean; details: string }[];
}

export function computeDegreeAudit(
  program: BenhaProgram,
  completedCourseCodes: string[],
  completedCourseCredits: Record<string, number>,
): DegreeAudit {
  const completed = new Set(completedCourseCodes.map((c) => c.trim().toLowerCase()));

  const matchCode = (c: BenhaCourse) =>
    completed.has(c.code.toLowerCase()) ||
    completedCourseCodes.some((cc) => cc.toLowerCase().includes(c.code.toLowerCase().replace(/\s/g, "")));

  const compAudit: CourseAuditItem[] = program.compulsoryPool.map((c) => ({
    course: c,
    type: "compulsory" as const,
    status: matchCode(c) ? "completed" : "pending",
    userCredits: matchCode(c) ? (completedCourseCredits[c.code] ?? c.credits) : undefined,
  }));

  const electAudit: CourseAuditItem[] = program.electivePool.map((c) => ({
    course: c,
    type: "elective" as const,
    status: matchCode(c) ? "completed" : "pending",
    userCredits: matchCode(c) ? (completedCourseCredits[c.code] ?? c.credits) : undefined,
  }));

  const trainingDone = completedCourseCodes.some(
    (cc) => cc.toLowerCase().includes(program.trainingCode.toLowerCase().replace(/\s/g, "")),
  );
  const researchDone = completedCourseCodes.some(
    (cc) => cc.toLowerCase().includes(program.researchCode.toLowerCase().replace(/\s/g, "")),
  );

  const compCompleted = compAudit
    .filter((a) => a.status === "completed")
    .reduce((s, a) => s + a.course.credits, 0);

  const electCompleted = electAudit
    .filter((a) => a.status === "completed")
    .reduce((s, a) => s + a.course.credits, 0);

  const totalCompleted = compCompleted + electCompleted + (trainingDone ? 3 : 0) + (researchDone ? 3 : 0);

  const cr = program.creditReq;
  const totalRequired = cr.specializationCompulsory + cr.specializationElective + cr.training + cr.research;

  return {
    programId: program.id,
    totalRequired,
    totalCompleted,
    totalRemaining: Math.max(0, totalRequired - totalCompleted),
    compulsoryRequired: cr.specializationCompulsory,
    compulsoryCompleted: compCompleted,
    electiveRequired: cr.specializationElective,
    electiveCompleted: electCompleted,
    freeElectiveRequired: cr.freeElective,
    freeElectiveCompleted: 0,
    trainingCompleted: trainingDone,
    researchCompleted: researchDone,
    progressPercent: Math.min(100, Math.round((totalCompleted / totalRequired) * 100)),
    completedCourses: [...compAudit, ...electAudit].filter((a) => a.status === "completed"),
    pendingCourses: [...compAudit, ...electAudit].filter((a) => a.status === "pending"),
    specialRequirements: [
      {
        label: "Field Training (3 cr)",
        met: trainingDone,
        details: program.trainingCode,
      },
      {
        label: "Research & Essay (3 cr)",
        met: researchDone,
        details: program.researchCode,
      },
    ],
  };
}
